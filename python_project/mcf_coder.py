def decrypt_byte(num, column=Col.EFirstCol):
    for i, t in enumerate(TABLE):
        try:
            if t.index(num) == column.value:
                return i
        except ValueError:
            continue
    print(f"Error decrypting 0x{num:X} in column {column.value}")
    return 0


def encrypt_byte(num, column=Col.EFirstCol):
    return TABLE[num][column.value]


def decrypt_area(data, start_col=Col.EFirstCol):
    out = bytearray(len(data))
    pos = start_col.value
    for i, b in enumerate(data):
        out[i] = decrypt_byte(b, Col(pos))
        pos += 1
        if pos > 3:
            pos = 0
    return out


